    private void btnNumeroClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNumeroClick
      
        if(clavierNumeriqueDestin == CLAVIER_NUMERIQUE_CODECLIENT){
            //On recupère le numero cliqué
            String btnClique = evt.getActionCommand();
            //On recupere la valeur du txtCodeClient plus le numero choisi
            String txtCodeClient = txtCodeClient1.getText() + btnClique;
            //On retire les dernies 6 caractères.
            String txtAAfficher = txtCodeClient.substring(txtCodeClient.length() - 6);
            //On affiche à l'ecran
            txtCodeClient1.setText(txtAAfficher);
        }
        else if(clavierNumeriqueDestin == CLAVIER_NUMERIQUE_CODENIP){
            //On recupère le numero cliqué
            String btnClique = evt.getActionCommand();
            //On recupere la valeur du txtCodeClient plus le numero choisi
            String txtCodeNIP = txtNIPClient.getText() + btnClique;
            String txtAAfficher = txtCodeNIP;
            //On retire les dernies 4 caractères.
            if(txtCodeNIP.length() > 4)
            txtAAfficher = txtCodeNIP.substring(txtCodeNIP.length() - 4);
            //On affiche à l'ecran
            txtNIPClient.setText(txtAAfficher);
        }
        
        
        
        
        
    }//GEN-LAST:event_btnNumeroClick

