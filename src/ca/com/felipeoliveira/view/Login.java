    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        codeClientSaisi = txtCodeClient1.getText();
        nipSaisi = txtNIPClient.getText() ;
        
        boolean admin = (nipSaisi.equals("admin")&& codeClientSaisi.equals("admin"));
        if(admin){
            //On initialise l'ecranAdmin
            EcranAdmin ecranAdmin = new EcranAdmin();
            ecranAdmin.setVisible(true);
            this.setVisible(false);
        }
        else{
        String query = "SELECT * FROM tbl_clients WHERE codeClient = '" + codeClientSaisi + "';";
        
        connexion.connect();
        statement = connexion.statement();
        try{
            resultSet = statement.executeQuery(query);
            
            if(resultSet.next())
            {
                int essaieLogin = resultSet.getInt("essaieLogin");
                String NIP = resultSet.getString("nip");
                if(essaieLogin > 2){
                    //Compte bloquee
                     frame.compteBloquee();
                }
                else{
                    if(nipSaisi.equalsIgnoreCase(NIP)){
                        query = "UPDATE tbl_clients SET essaieLogin = 0 WHERE codeClient = '"+ codeClientSaisi + "';";
                        statement.execute(query);
                        //On initialise l'ecranClient
                        EcranClient ecranClient = new EcranClient(codeClientSaisi);
                        ecranClient.setVisible(true);
                        this.setVisible(false);
                    }
                    else{
                       //On ajoute un erreur
                       essaieLogin++;
                       query = "UPDATE tbl_clients SET essaieLogin = " + essaieLogin + " WHERE codeClient = '"+ codeClientSaisi + "';";
                       statement.execute(query);
                    }
                }
                
                
            }

       
        }catch(SQLException e){
            e.printStackTrace();
        }
        finally{
         connexion.deconnecter();
        }
        }         
    }//GEN-LAST:event_btnOkActionPerformed

